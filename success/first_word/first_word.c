#include <unistd.h>

int	main(int ac, char **av)
{
	int	c = 0;

	if (ac == 2)
	{
		while (av[1][c] == 9 || av[1][c] == 32)
			c++;
		while (av[1][c])
		{
			if(av[1][c] == 9 || av[1][c] == 32)
				break ;
			write(1, &av[1][c], 1);
			c++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
